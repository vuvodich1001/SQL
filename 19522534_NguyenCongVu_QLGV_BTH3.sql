UPDATE HOCVIEN
SET DIEMTB = (SELECT AVG(DIEM) FROM KETQUATHI K1 
					WHERE LANTHI = (SELECT MAX(LANTHI) FROM KETQUATHI K2 
					GROUP BY MAHV, MAMH 
					HAVING K1.MAHV = K2.MAHV AND K1.MAMH = K2.MAMH)
			GROUP BY MAHV
			HAVING HOCVIEN.MAHV = MAHV
)

